// p_brust → lx-cat-brust (Klassen-Namen für Farbe)
export function _lxCatClass(catKey) {
  return 'lx-cat-' + (catKey || '').replace(/^p_/, '');
}

// Flache Liste aller Übungen mit Kategorie-Key
// Default-Location ist 'studio' — Calisthenics-Übungen überschreiben das
// explizit mit 'home' oder 'outdoor'.
export function _lxAllExercises() {
  const list = [];
  Object.entries(LEXIKON_DATA).forEach(([catKey, exercises]) => {
    exercises.forEach(ex => list.push({
      ...ex,
      catKey,
      location: ex.location || 'studio'
    }));
  });
  return list;
}

